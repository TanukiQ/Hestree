import UIKit

extension UIFont {
    class func chatTextFont() -> UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    
    class func skillDiscoverTextFont() -> UIFont {
        return UIFont.systemFont(ofSize: 11)
    }
}
